class Cab{
	String driver;
	String brand;
	String model;
	String color;
	String regNo;
	String fuelType;
	int seater;
	String cabCompany;
	boolean roofCarrier;
	int rating;
	boolean gps;
	int speed;
	int airbags;
	int fuelTankCapacity;
	boolean childLock;
	boolean autoBraking;
	boolean cam;	
	String pickUp;
	String dest;
	int price;	
	
	Cab(String driver,String brand,String model,String color,String regNo,String fuelType,int seater,String cabCompany,boolean roofCarrier,int rating,boolean gps,int speed,int airbags,int fuelTankCapacity,boolean childLock,boolean autoBraking,boolean cam,String pickUp,String dest,int price){
		this.driver=driver;
		this.brand=brand;
		this.model=model;
		this.color=color;
		this.regNo=regNo;
		this.fuelType=fuelType;
		this.seater=seater;
		this.cabCompany=cabCompany;
		this.roofCarrier=roofCarrier;
		this.rating=rating;
		this.gps=gps;
		this.speed=speed;
		this.airbags=airbags;
		this.fuelTankCapacity=fuelTankCapacity;
		this.childLock=childLock;
		this.autoBraking=autoBraking;
		this.cam=cam;
		this.pickUp=pickUp;
		this.dest=dest;
		this.price=price;
	}
}























