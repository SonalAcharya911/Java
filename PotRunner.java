class PotRunner{
	public static void main(String[] args){
		String pot1="Flower pot";
		String pot2="Mudpot";
		String pot3="Earthen pot";
		String pot4="Clay pot";
		String pot5="Metal pot";
		
		String[] pots={pot1,pot2,pot3,pot4,pot5};
		
		for(String pot: pots){
			System.out.println(pot);
		}
	}
}